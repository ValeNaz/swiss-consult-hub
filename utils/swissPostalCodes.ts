export const getCityByPostalCode = (postalCode: string): string | null => {
    const cities = SWISS_POSTAL_CODES[postalCode];
    return cities ? cities[0] : null;
};

export const isValidSwissPostalCode = (postalCode: string): boolean => {
    return /^\d{4}$/.test(postalCode) && parseInt(postalCode) >= 1000 && parseInt(postalCode) <= 9999;
};
